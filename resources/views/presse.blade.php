            <h2>St. Patrick's Night</h2>
            <h3>Pressetext</h3>
            <h4>St. Patrick's Night: Irish Music & Show Dance im Orpheum Wien</h4>
<p>
            Irisches Pub-Feeling in der St. Patrick's Night am 10. März 2018. Paddy's Return (Wien) und Patrick's Holm (Dublin) begeistern die Fans mit irischen Tunes und Songs. Die Shamrock Dance Company Vienna leistet dazu die authentische Beinarbeit.
</p>
            <p>
            <b>Paddy's Return</b> mit Gregor Lötsch (Gesang, Gitarre), Günther Hackl (Fiddle, Gesang), Thomas Blazek (Flöte, Whistle), Xandi Tichy (Akkordeon, Pipes) und Michael Strasser (Bodhrán, Gesang) eröffnen in bekannter Manier mit Vollgas den Abend. Vom Little Beggarman bis zum Irish Rover alles mitreißend intoniert und interpretiert - irischer geht's nicht. Mit Mitsinggarantie!
            </p>
            <p>
                Für bekannte irische Standards und auch etwas ruhigere Töne wird <b>Singer-Songwriter Paul Kelly</b> sorgen, der mit <b>Patrick's  Holm</b> (Carol Keane, Fiddle, und den All-Ireland Champions Alan Byrne, Banjo und Maurice Egan, Akkordeon) zum ersten Mal in Österreich gastiert. In seinen Eigenkompositionen singt Paul Kelly mit klarer schwebender Stimme von der großen unverbrüchlichen unerfüllt gebliebenen Liebe der kleinen Leute, von den großen Träumen der Auswanderer, von Orten und Menschen - vom Mikrokosmos des Lebens.
            </p>
            <p>
            <b>Die Shamrock Dance Company Vienna</b> hat seit ihrer Gründung im Jahr 1999 immer wieder Europameisterinnen hervorgebracht. Bühnenauftritte zu Live-Musik sind eher die Ausnahme. Nicht zuletzt deshalb ist die St. Patrick's Night 2018 eine Besonderheit.
            Prädikat: Absolut sehens- und hörenswert!
            </p>
