            <h2>St. Patrick's Night</h2>
            <h3>Pressetext</h3>
            <h4>St. Patrick's Night: Irish Music & Show Dance im Orpheum Wien</h4>
<p>
            Irisches Pub-Feeling in der St. Patrick's Night am 8. März 2019. Paddy's Return (Wien) und das Four Star Trio (Cork, Irland) begeistern die Fans mit irischen Tunes und Songs. Die Shamrock Dance Company Vienna leistet dazu die authentische Beinarbeit.
</p>
            <p>
            <b>Paddy's Return</b> mit Gregor Lötsch (Gesang, Gitarre), Günther Hackl (Fiddle, Gesang), Thomas Blazek (Flöte, Whistle), Xandi Tichy (Akkordeon, Pipes) und Michael Strasser (Bodhrán, Gesang) eröffnen in bekannter Manier mit Vollgas den Abend. Vom Little Beggarman bis zum Irish Rover alles mitreißend intoniert und interpretiert - irischer geht's nicht. Mit Mitsinggarantie!
            </p>
            <p>
                Das <b>Four Star Trio</b> zählt mit 30 Jahren Bühnenroutine in ganz Europa zu den erfahrensten aktiven
irischen Bands. Con Ó Drisceoil (Button-Akkordeon, Gesang), Johnny McCarthy (Fiddle, Flöte,
Gesang) und Patrick Ahern (Gitarre) werden das erste Mal in Österreich zu hören sein. Bekannt
für seine humoristischen Songs wird Con Ó Drisceoil seine Liederbücher samt CDs im Gepäck
haben; mit dabei ist natürlich auch die aktuelle CD des Four Star Trio „Magnetic South“.
            </p>
            <p>
            <b>Die Shamrock Dance Company Vienna</b> hat seit ihrer Gründung im Jahr 1999 immer wieder Europameisterinnen hervorgebracht. Bühnenauftritte zu Live-Musik sind eher die Ausnahme. Nicht zuletzt deshalb ist die St. Patrick's Night 2019 eine Besonderheit.
            Prädikat: Absolut sehens- und hörenswert!
            </p>
