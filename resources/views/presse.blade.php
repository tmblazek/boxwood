<h3>Pressetext</h3>
<p>In einer der langen Sessionnächte im Peter’s Old Oak Pub entstand die Idee die Irish Folkband Paddy’s Return zu gründen, benannt nach einem irischen Jig und nach dem wohlschmeckenden Paddy’s Irish Whiskey. Die musikalische Ausrichtung ist irlandlastig, wobei sich die Band auch schottische & südeuropäische Klänge erlaubt. Songs und Instrumentaltunes werden in einer ausgewogenen Mischung gespielt und gesungen.</p>
