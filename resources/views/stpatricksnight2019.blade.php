<h2>Das Event</h2>
<p>Bereits das fünfte Jahr laden „Paddy’s Return“ sich am <a href="https://www.paddysreturn.com/konzerte/29">8. März</a> Gäste ein und eröffnen mit der gemeinsamen „St. Patrick’s Night“ das auch in Wien schon recht bunte Treiben rund um den St. Patrick’s Day, den irischen Nationalfeiertag.
Zum ersten Mal in Wien zu sehen und zu hören ist dabei das „Four Star Trio“ aus Cork, das eine Woche später das 30-jährige Bühnenjubiläum feiern wird. Die authentische Beinarbeit zur Musik kommt wieder von der „Shamrock Dance Company Vienna“.
Die Organisatoren und die eingeladenen Gäste sorgen dafür, das irische Flair und die dazugehörige Stimmung möglichst echt und ehrlich zu vermitteln. Wichtiger Bestandteil des Abends ist auch das Publikum, das bei dieser Veranstaltung keineswegs zum stillen Zuhören und Zusehen angehalten ist. Vielmehr gilt es, das Orpheum gemeinsam in das größte irische Pub Österreichs zu verwandeln.
Con O’Drisceoil vom „Four Star Trio“ wird durch den Abend führen.


<h2>Die Show</h2>

<p>Zwei Bands und eine Tanzgruppe sorgen mit jeder Menge Musik und Tanz f&uuml;r
irische Stimmung. Das Publikum ist bei dieser Veranstaltung wichtiger
Bestandteil des Abends und keineswegs zur passiven Konsumation des Programms
gezwungen. Ziel ist es, ein paar Stunden lang das gr&ouml;&szlig;te Pub
&Ouml;sterreichs zu sein. Die Band &bdquo;Paddy&rsquo;s Return&ldquo; als
Gastgeber und Opener und die eingeladenen G&auml;ste sorgen daf&uuml;r, das
irische Flair und die dazugeh&ouml;rige Stimmung m&ouml;glichst ehrlich und
ungek&uuml;nstelt zu vermitteln.&nbsp;</p>

<p>Das Repertoire der einzelnen Gruppen garantiert einen abwechslungsreichen
Abend, der durch das Spektrum der irischen Tanz- und Musiktradition f&uuml;hrt.</p>

<p>Das &bdquo;<strong>Four Star Trio</strong>&ldquo; istammt aus dem County Cork im Südwesten der grünen Insel und blickt auf knapp 30 Jahre Erfahrung in unveränderter Besetzung zurück.</p>

<p>Die &bdquo;<strong>Shamrock Dance Company Vienna</strong>&ldquo; wiederum ist ein gro&szlig;e Irish Dance Vereinigung in Wien mit starken Kontakten Nach Irland, die schon Europameisterinnen hervorgebracht hat.</p>

<p>&bdquo;<strong>Paddy&rsquo;s Return</strong>&ldquo; sind die einladenden Organisatoren der Show. Details zur Band finden Sie hier auf diesen Seiten.</p>

<p>Unterhaltung ist also garantiert.</p>
